package com.ejamku.model;

public class CalonNasabah {

    private String nik;
    private String nama;
    private String npwp;
    private String kode_kabkota;
    private String kode_pos;

    public String getNik() {
        return nik;
    }

    public void setNik(String nik) {
        this.nik = nik;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getNpwp() {
        return npwp;
    }

    public void setNpwp(String npwp) {
        this.npwp = npwp;
    }

    public String getKode_kabkota() {
        return kode_kabkota;
    }

    public void setKode_kabkota(String kode_kabkota) {
        this.kode_kabkota = kode_kabkota;
    }

    public String getKode_pos() {
        return kode_pos;
    }

    public void setKode_pos(String kode_pos) {
        this.kode_pos = kode_pos;
    }
}
